bicycles = ['trek', 'cannondale', 'redline', 'specialized']

# Returns entire ugly list of bicycles
print(bicycles)
print("")

# Returns the first element from the list
print(bicycles[0])
print("")

# Returns the first element from the list capitalized
print(bicycles[0].title())
print("")

# Returns the second and fourth element of the list
print(bicycles[1])
print(bicycles[3])
print("")

# Returns the last element from the list
print(bicycles[-1])
print("")

# Returns the second to last element from the list
print(bicycles[-2])
print("")

# Returns the third from last element from the list
print(bicycles[-3])
print("")

# Prints the message using f sharp literal
message = f"My first bicycle was a {bicycles[0].title()}."
print(message)
print("")
